public class Airplane {

    private boolean canFly;
    private int howManyPilotsNeeded;
    private String model;
    
    public boolean isCanFly() {
        return canFly;
    }
    public void setCanFly(boolean canFly) {
        this.canFly = canFly;
    }
    public int getHowManyPilotsNeeded() {
        return howManyPilotsNeeded;
    }
    public void setHowManyPilotsNeeded(int howManyPilotsNeeded) {
        this.howManyPilotsNeeded = howManyPilotsNeeded;
    }
    public String getModel() {
        return model;
    }
    public void setModel(String model) {
        this.model = model;
    }

    

}
